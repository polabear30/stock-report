def get_events_for_date(target: date) -> List[Dict[str, Any]]:
    """특정 날짜의 이벤트 목록을 반환한다."""
    ds = target.isoformat()
    return [e for e in CALENDAR_EVENTS if e["날짜"] == ds]


def get_events_for_range(start: date, end: date) -> List[Dict[str, Any]]:
    """날짜 범위의 이벤트 목록을 반환한다."""
    s, e = start.isoformat(), end.isoformat()
    return [ev for ev in CALENDAR_EVENTS if s <= ev["날짜"] <= e]


def get_events_next_24h() -> List[Dict[str, Any]]:
    """향후 24시간 이내 이벤트를 반환한다."""
    today = date.today()
    tomorrow = today + timedelta(days=1)
    return get_events_for_range(today, tomorrow)


def get_month_summary(year: int, month: int) -> Dict[str, List[Dict[str, Any]]]:
    """해당 월의 날짜별 이벤트를 딕셔너리로 반환한다."""
    import calendar
    _, last_day = calendar.monthrange(year, month)
    start = date(year, month, 1)
    end = date(year, month, last_day)
    events = get_events_for_range(start, end)

    by_date: Dict[str, List[Dict[str, Any]]] = {}
    for ev in events:
        by_date.setdefault(ev["날짜"], []).append(ev)
    return by_date


def get_event_count_by_date(year: int, month: int) -> Dict[str, Dict[str, int]]:
    """해당 월의 날짜별 카테고리별 이벤트 수를 반환한다."""
    month_events = get_month_summary(year, month)
    result = {}
    for d, evts in month_events.items():
        cats: Dict[str, int] = {}
        for ev in evts:
            cat = ev["카테고리"]
            cats[cat] = cats.get(cat, 0) + 1
        result[d] = cats
    return result
